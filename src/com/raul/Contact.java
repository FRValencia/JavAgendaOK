package com.raul;

public class Contact {
    private String name;            //esto es una variable de instancia, cada una tiene un valor diferente
    private String phone;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
